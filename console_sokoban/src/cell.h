#ifndef INCLUDED_CELL_H
#define INCLUDED_CELL_H
/**
* @file cell.h
* セルクラスの記述 "src/cell.h"
* @details Stageクラスにcellクラスを一つ持たせてステージデータとして使う
* 引数付きのコンストラクタ呼び出し以外今の所機能しない。
* @author YouTE
* @date 2019/05/03
*/

#include <vector>
#include "game_lib.h"
template <typename T>class Cell{
public:
	/**
	* デフォルトコンストラクタ
	* @details データメンバのwidth_,height_を0初期化しておく。<br>
	* 但しこの初期化をした場合はデータメンバ自体が0初期化しかされてないので、何処かでwidth_とheight_とarray_を代入しなければならない
 	*/
	Cell()
		: width_(0), height_(0){
		array_.resize(width_ * height_);
	}
	/**
	* 引数付きコンストラクタ
	* @param [in] arg_width データメンバのwidth_の初期化
	* @param [in] arg_height データメンバのheight_の初期化
	*/
	Cell(int arg_width, int arg_height) 
		: width_(arg_width), height_(arg_height){
		array_.resize(width_ * height_);
	}
	/**
	* 新しく横幅と縦幅と array_配列のリサイズ
	* @param [in] arg_width
	* @param [in] arg_height
	*/
	void SetSize(int arg_width, int arg_height) {
		width_ = arg_width;
		height_ = arg_height;
		array_.resize(width_ * height_);
	}
	/**
	* 横幅の取得
	* @return width_ 横幅
	*/
	int GetWidth() const{
		return width_;
	}
	/**
	* 縦幅の取得
	* @return height_ 縦幅
	*/
	int GetHeight() const{
		return height_;
	}
	/**
	* 扱いやすいように演算子のオーバーロードを作成
	* @details 範囲外を指す事ができる為使い方に注意
	* TODO:expectionを今後追加予定
	* @param [in] x_pos 横の位置
	* @param [in] y_pos 縦の位置
	* @return 変更不可能なデータを返す
	*/
	const T& operator()(int x_pos, int y_pos) const{
		if (0 > (x_pos + y_pos * width_) ||
			static_cast<int>(array_.size()) <= (x_pos + y_pos * width_)) {
			throw("範囲外にアクセス");
		}
		return array_.at(x_pos + y_pos * width_);
	}
	/**
	* 扱いやすいように演算子のオーバーロードを作成
	* @details 範囲外を指す事ができる為使い方に注意
	* @param [in] x_pos 横の位置
	* @param [in] y_pos 縦の位置
	* @return 変更可能なデータを返す
	*/
	T& operator()(int x_pos, int y_pos) {
		if (0 > (x_pos + y_pos * width_) ||
			static_cast<int>(array_.size()) <= (x_pos + y_pos * width_)) {
			throw("範囲外にアクセス");
		}
		return array_.at(x_pos + y_pos * width_);
	}

	const T& operator()(int pos)const {
		if (0 > pos ||
			static_cast<int>(array_.size()) <= pos) {
			throw("範囲外にアクセス");
		}
		return array_.at(pos);
	}
	T& operator()(int pos){
		if (0 > pos ||
			static_cast<int>(array_.size()) <= pos) {
			throw("範囲外にアクセス");
		}
		return array_.at(pos);
	}

private:
	std::vector<T> array_; ///<ステージデータ
	int width_; ///<横幅
	int height_; ///<縦幅
};
#endif //INCLUDED_CELL_H